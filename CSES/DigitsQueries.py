q = int(input())
while(q):
    k = int(input())
    if(k < 10):
        print(k)
        q -= 1
        continue
    idx = 0
    for i in range(len(ranges)):
        if(k <= ranges[i][1]):
            idx = i
            break
    l = ranges[idx][0]
    a = k - l
    digits = idx
    div = a // (10*digits)
    new_s = div * 10 + 10**(digits-1)
    new_idx = a - div*(10*digits)
