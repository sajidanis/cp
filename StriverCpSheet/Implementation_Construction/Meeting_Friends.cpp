void solve() {
    ll a, b, c;
    cin >> a >> b >> c;

    ll max_el = max(a, max(b, c));
    ll min_el = min(a, min(b, c));

    ll mid_el = a + b + c - max_el - min_el;
    ll ans = (max_el - mid_el) + (mid_el - min_el);
    cout << ans;
    return;
}

